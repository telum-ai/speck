#!/usr/bin/env bash
# compute-eval-signals.test.sh — smoke tests for VCS-as-eval metric computer

set -euo pipefail

ROOT="$(cd "$(dirname "$0")/../../../../" && pwd)"
VALIDATOR="$ROOT/.speck/scripts/validation/validators/compute-eval-signals.sh"
TMP="$(mktemp -d)"
trap 'rm -rf "$TMP"' EXIT

# Set up clean temporary git repository
cd "$TMP"
git init
git config user.name "Human Engineer"
git config user.email "human@speck.dev"

# Helper to commit
make_commit() {
  local author_name="$1"
  local author_email="$2"
  local msg="$3"
  local filename="$4"
  local content="$5"

  echo "$content" >> "$filename"
  git add "$filename"
  # Commit with explicit author
  git -c "user.name=$author_name" -c "user.email=$author_email" commit -m "$msg"
}

echo "Test 1: Check info mode on empty repo"
# Since we just ran git init and there are no commits, should print empty/pass info safely
bash "$VALIDATOR" "$TMP"
echo "  ✓ Passed Test 1"

echo "Test 2: Check standard commits (no agents)"
make_commit "Human User" "human@speck.dev" "feat: initial commit" "app.js" "console.log('hello');"
make_commit "Therese Key" "therese@speck.dev" "docs: update specs" "README.md" "Spec info"
bash "$VALIDATOR" "$TMP"
echo "  ✓ Passed Test 2"

echo "Test 3: Check agent commits (stable, no reverts)"
make_commit "Claude Agent" "claude@ai.speck.dev" "feat(api): autonomous endpoint" "api.js" "// generated by Claude"
# Should pass, override rate is 0%
bash "$VALIDATOR" --threshold 20 "$TMP"
echo "  ✓ Passed Test 3"

echo "Test 4: Check agent commits with a revert (high override rate, strict fails)"
# Let's revert the Claude Agent commit
make_commit "Human User" "human@speck.dev" "revert: revert Claude Agent commit \"feat(api): autonomous endpoint\"" "api.js" "// human override"

# Now we have 1 agent commit, and 1 human revert referencing its subject.
# Total agent commits: 1
# Total reverted: 1
# Override rate: 100%
# Threshold: 20%
# Strict mode should fail with exit code 1.
if bash "$VALIDATOR" --threshold 20 --strict "$TMP" >/dev/null 2>&1; then
  echo "ERROR: Expected strict mode to fail due to 100% override rate, but it passed!"
  exit 1
else
  echo "  ✓ Passed Test 4 (failed correctly in strict mode)"
fi

echo "Test 5: Check agent commits with a revert (under threshold, strict passes)"
# Let's add more agent commits that are not reverted to bring override rate down
# Current state: 1 reverted agent commit
# Let's add 4 stable agent commits to make it 1 out of 5 = 20% override rate.
make_commit "Claude Agent" "claude@ai.speck.dev" "feat: stable AI commit 1" "foo.js" "code"
make_commit "Claude Agent" "claude@ai.speck.dev" "feat: stable AI commit 2" "bar.js" "code"
make_commit "Claude Agent" "claude@ai.speck.dev" "feat: stable AI commit 3" "baz.js" "code"
make_commit "Claude Agent" "claude@ai.speck.dev" "feat: stable AI commit 4" "qux.js" "code"

# Now agent commits = 5, reverted agent commits = 1.
# Override rate = 20%. Threshold = 25%.
# Strict mode should pass.
bash "$VALIDATOR" --threshold 25 --strict "$TMP"
echo "  ✓ Passed Test 5"

echo "✅ All compute-eval-signals smoke tests passed!"
exit 0
